// ── Main ─────────────────────────────────────────────────────────────────────

const FILE = "data/fiscal-budget-links.ts";
let src = readFileSync(FILE, "utf-8");

let totalCities = 0;
let totalCounties = 0;

for (const [province, cities] of Object.entries(COUNTY_DATA)) {
  for (const [cityName, counties] of Object.entries(cities)) {
    // Build the children string
    const childrenLines = counties
      .map((c) => `          { name: "${c}", url: "" },`)
      .join("\n");

    // Pattern: match the inline city entry within the province section
    // Handle both `{ name: "XX", url: "YY" },` and `{ name: "XX", url: "YY" }` (last item)
    // The city must be at 6-space indentation (child of province children array)
    const escaped = cityName.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const re = new RegExp(
      `^(      \\{ name: "${escaped}", url: )(".+?"|"") \\},?`,
      "m"
    );

    const match = src.match(re);
    if (!match) {
      console.error(`❌ Could not find: ${province} → ${cityName}`);
      continue;
    }

    const url = match[2]; // the url value including quotes
    const trailingComma = match[0].endsWith(",") ? "," : "";

    const replacement = [
      `      {`,
      `        name: "${cityName}",`,
      `        url: ${url},`,
      `        children: [`,
      childrenLines,
      `        ],`,
      `      }${trailingComma}`,
    ].join("\n");

    src = src.replace(match[0], replacement);
    totalCities++;
    totalCounties += counties.length;
    console.log(`✅ ${province} → ${cityName} (${counties.length} counties)`);
  }
}

writeFileSync(FILE, src, "utf-8");
console.log(`\nDone! Added children to ${totalCities} cities (${totalCounties} total county entries).`);
